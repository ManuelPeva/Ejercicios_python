
'''Utiliza la función max para concluir cual de los valores es mayor'''

num1 = int(input("Digite el valor de su primer numero: "))

num2 = int(input("Digite el valor del segundo numero: "))

num3 = int(input("Digite un tercer valor: "))

mas_alto = max(num1,num2,num3)

print(f"El número mayor es: {mas_alto}")

